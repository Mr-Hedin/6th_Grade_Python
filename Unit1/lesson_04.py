# Now that you know about data types, we can start to use variables in a useful way.


# First, let's create a variable that stores a string:

my_string = "Hello World!"

# We can use this variable to change the message we want to print out!

print(my_string)

# If you use the same variable name and assign it a new value, it will overwrite the previous value!

my_string = "Goodbye World!"

print(my_string)


